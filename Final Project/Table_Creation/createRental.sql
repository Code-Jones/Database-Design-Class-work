create table RENTAL (
Rental_ID  			NUMBER(5) GENERATED AS IDENTITY (START WITH 40001 INCREMENT BY 1),
Title_ID 			NUMBER(5),
Customer_ID			NUMBER(5),
Rental_Format		CHAR(2) 	CONSTRAINT RENTAL_FORMAT_NN NOT NULL,
Rental_Price		NUMBER(6,2) CONSTRAINT RENTAL_PRICE_NN NOT NULL,
Rental_Time			DATE 		CONSTRAINT RENTAL_TIME_NN NOT NULL, 
Rental_Start		DATE 		CONSTRAINT RENTAL_START_NN NOT NULL,
Rental_End			DATE 		CONSTRAINT RENTAL_END_NN NOT NULL, 
Rental_Star_Rating	Number(2,1),
CONSTRAINT RENTAL_FORMAT_CK 	CHECK (RENTAL_FORMAT IN ('SD', 'HD')),
CONSTRAINT RENTAL_START_CK 		CHECK (RENTAL_START >= RENTAL_TIME),
CONSTRAINT RENTAL_END_CK 		CHECK (RENTAL_END > RENTAL_START),
CONSTRAINT RENTAL_STAR_RATING_CK CHECK (Rental_Star_Rating >= 0 AND Rental_Star_Rating <= 5.1)
);

